								Posisi power supply sangat beragam dan bergantung dengan jenis case yang digunakan. Pasang power supply sesuai tempat yang disediakan. 
							</p>
							<p class="materi-content-text">
								Setelah dirasa pas, kencangkan body power supply dengan baut hingga posisi power supply kuat. power supply disini berguna untuk memberi daya atau arus listrik pada komputer.
							</p>
							<h5 class="point-tittle">
								6. Memasang Motherboard ke Casing
							</h5>
							<p class="materi-content-text">
								Setelah power supply terpasang langkah selanjutnya adalah memasang motherboard kesalam casing. Laakukan secara perlahan dan hati-hati agar tidak terjadi hal yang tidak diinginkan. Setelah motherboard berada pada posisinya, kencangkan dengan mur atau baut.
							</p>
							<h5 class="point-tittle">
								7. Memasang DVD RW (Optic Drive)
							</h5>
							<p class="materi-content-text">
								Pemasangan CD/DVD Drive biasanya terletak di bagian depan casing. Letakkan CD/DVD Drive pada tempatnya dan kencankan dengan mur atau baut. Lalu, pasang kabel yang menghubungkan antara CD/DVD Drive dengan konektor pada motherboard.
							</p>
							<h5 class="point-tittle">
								8. Memasang Hardisk
							</h5>
							<p class="materi-content-text">
								Untuk memasang, silahkan masukan Hardisk. Yang ada labelnya menghadap keatas. Posisikan lubang harddisk dengan casing. Lalu, kencangkan dengan menggunakan mur atau baut.
							</p>
							<h5 class="point-tittle">
								9. Memasang konektor USB dan Audio
							</h5>
							<p class="materi-content-text">
								Agar keduanya konektor bisa aktif maka konektornya harus dipasang pada Motherboard. Pasang dengan cara melihat bentuk konektor dan cari jumper yang memiliki tulisan USB dan AUDIO. Biasanya dibagian pinggir Motherboard.
							</p>
							<h5 class="point-tittle">
								10. Memasang kabel Front Panel
							</h5>
							<p class="materi-content-text">
								Kabel ini terdiri dari beberapa bagian seperti kabel power, kabel restart, dan kabel led. Tiap bagiannya memiliki fungsi masing-masing. Untuk memasangnya perhatikan keterangan yang ada di motherboard, biasanya posisinya berdempetan. Setelah diketahui posisinya, pasang kabel kecil tersebut secara perlahan dan perhatikan juga letak jumper yang ada.
							</p>
							<h5 class="point-tittle">
								11. Memasang kabel dari Power Supply ke Motherboard
							</h5>
							<p class="materi-content-text">
								Dalam memasang kabel power supply pastikan kamu memposisikannya dengan pas. Karena kabel inilah yang akan menghubungkan arus yang akan masuk. Pastikan kamu memasangnya dengan pas dan jangan sampai longgar.
							</p>
							<h5 class="point-tittle">
								12. Menyambungkan Kabel Arus Listrik
							</h5>
							<p class="materi-content-text">
								Kabel yang pertama adalah kabel arus listrik. ujung kabel arus tersebut memiliki tiga lubang, silahkan colokan ke tempatnya pada Power Supply yang digunakan untuk sambungan parallel ke monitor.
							</p>
							<h5 class="point-tittle">
								13. Menyambungkan ke Monitor
							</h5>
							<p class="materi-content-text">
								Sambungkan kabel dari Monitor ke VGA Card. Konektor VGD dan monitor memiliki bentuk agak mengecil disebelah bawah mirip trapesium dengan ujung tumpul (tidak persegi panjang). Didalamnya terdapat tiga baris jarum-jarum (pin) kecil. Jadi, pasang konektor ketempat yang pas.
							</p>
							<h5 class="point-tittle">
								14. Test Komputer/PC
							</h5>
							<p class="materi-content-text">
								Setelah semua selesai dan terpasang dengan baik, maka sekarang lakukan uji coba pada komputer. Ujicoba yang dilakukan seperti cek tombol power, restart, led hingga port USB. Setelah melakukan ujicoba dan kamu berhasil masuk ke tampilan booting OS maka kamu berhasil. Apabila komputer tidak menyala, perhatikan kabel power supply sudah pas atau belum. Apabila komputer nyala tetapi blank maka pastikan sambungan kabel ke monitor, RAM, dan processor hingga benar-benar pas.
